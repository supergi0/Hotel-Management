int Datetimebasic(String min,String hrs,String a,String b,String c){
int Days=Integer.parseInt(a);
int Months=0;
switch(b){
case "jan": Months=1*31;
break;
case "feb": Months=2*28;
break;
case "mar": Months=3*31;
break;
case "apr": Months=4*30;
break;
case "may": Months=5*31;
break;
case "jun": Months=6*30;
break;
case "jul": Months=7*31;
break;
case "aug": Months=8*31;
break;
case "sep": Months=9*30;
break;
case "oct": Months=10*31;
break;
case "nov": Months=11*30;
break;
case "dec": Months=12*31;
break;
}
int Years=Integer.parseInt(c);
int Hours=Integer.parseInt(hrs);
int Mins=Integer.parseInt(min);

return (Mins+60*(Hours+24*(Days+Months+365*(Years))));
}

